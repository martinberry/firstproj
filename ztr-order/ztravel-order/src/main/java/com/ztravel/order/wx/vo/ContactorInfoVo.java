package com.ztravel.order.wx.vo;

public class ContactorInfoVo {
	/**
	 * 常用联系人Id
	 * */
	private String comContactorId;
	/**
	 * 联系人姓名
	 * */
	private String name;
	/**
	 * 手机号
	 * */
	private String mobile;
	/**
	 * 邮件
	 * */
	private String mail;
	/**
	 * 省
	 * */
	private String province;
	/**
	 * 市
	 * */
	private String city;
	/**
	 * 县
	 * */
	private String county;
	/**
	 * 街道地址
	 * */
	private String street;
	public String getComContactorId() {
		return comContactorId;
	}
	public void setComContactorId(String comContactorId) {
		this.comContactorId = comContactorId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getMail() {
		return mail;
	}
	public void setMail(String mail) {
		this.mail = mail;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getCounty() {
		return county;
	}
	public void setCounty(String county) {
		this.county = county;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}

}
